n, m = map(int, input().split())

# 화폐의 가치 목록
cash = []
for _ in range(n):
    cash.append(int(input()))

# [10001, 10001, 10001, 10001, 10001, 10001, 10001, 10001]
d = [10001] * (m + 1)

d[0] = 0  # [0, 10001, 10001, 10001, 10001, 10001, 10001, 10001]
for i in cash:  # [2, 3, 5]
    # i = 2
    for j in range(i, m + 1):
        # j = 2 ~ 7 / 3 ~ 7 / 5 ~ 7
        if d[j - i] != 10001:  # j = 2
            # [0, 10001, 10001, 10001, 10001, 10001, 10001, 10001]
            # [0, 10001, 1, 10001, 2, 10001, 3, 10001]
            d[j] = min(d[j], d[j - i] + 1)

    # [0, 10001, 1, 10001, 2, 10001, 3, 10001]
    # [0, 10001, 1, 1, 2, 2, 2, 3]
    # [0, 10001, 1, 1, 2, 1, 2, 2]


if d[m] == 10001:
    print(-1)
else:
    print(d[m])
